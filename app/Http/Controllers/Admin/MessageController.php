    /**
     * Display the specified resource.
     *
     * @param  \App\Models\Messages  $messages
     * @return \Illuminate\Http\Response
     */
    public function show(Messages $messages)
    {
        //
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  \App\Models\Messages  $messages
     * @return \Illuminate\Http\Response
     */
    public function edit(Messages $messages,$id)
    {
        $data = Messages::find($id);
        $data->status = 'Read';
        $data->save();
        return view('back.contact_messages_edit',['data'=>$data]);
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  \App\Models\Messages  $messages
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, Messages $messages,$id)
    {
        $data = Messages::find($id);
        $data->note = $request->input('note');
        $data->save();
        return back()->with('info','Read');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  \App\Models\Messages  $messages
     * @return \Illuminate\Http\Response
     */
    public function destroy(Messages $messages,$id)
    {
        $data = Messages::find($id);
        $data->delete();
        return redirect()->route('admin_message')->with('info','Deleted');
    }
}
